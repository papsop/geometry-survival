int main()
{
  sf::RenderWindow window{sf::VideoMode(640, 480), "ImGui + SFML = <3"};
  window.setFramerateLimit(60);

  ImGui::SFML::Init(window);

  auto& io = ImGui::GetIO();
  io.ConfigFlags |= ImGuiConfigFlags_DockingEnable;

  // auto fancyFont = io.Fonts->AddFontFromFileTTF("./assets/hubballi-regular.ttf", 20);
  //   if (!ImGui::SFML::UpdateFontTexture()) {
  //     std::cerr << "No luck\n";
  //   }

  setFancyImguiStyle();

  sf::Clock deltaClock{};
  while (window.isOpen()) {
    sf::Event event{};
    while (window.pollEvent(event)) {
      ImGui::SFML::ProcessEvent(window, event);

      if (event.type == sf::Event::Closed) {
        window.close();
      }
    }

    ImGui::SFML::Update(window, deltaClock.restart());

    static ImVec4 circleColor{1.0f, 0.0f, 1.0f, 0.5f};
    static float circleRadius = 100.f;
    static std::size_t circlePoints = 30;

    sf::CircleShape shape{circleRadius, circlePoints};
    shape.setFillColor(circleColor);

    static ImVec2 viewportSize{500, 500};
    sf::RenderTexture rt{};
    rt.create(viewportSize.x, viewportSize.y);

    rt.clear(sf::Color::Black);
    rt.draw(shape);

    // ImGui::PushFont(fancyFont);
    ImGui::DockSpaceOverViewport();

    ImGui::ShowDemoWindow();

    if (ImGui::Begin("Circle manipulator")) {
      ImGuiColorEditFlags flags = ImGuiColorEditFlags_AlphaBar;
      ImGui::ColorPicker4("Circle Color", (float*)&circleColor, flags, nullptr);

      ImGui::DragFloat("Circle Radius", &circleRadius);
      ImGui::DragInt("Circle Points", (int*)&circlePoints, 1.0f, 3, 500);
    }
    ImGui::End();

    ImGui::PushStyleVar(ImGuiStyleVar_WindowPadding, ImVec2(0.f, 0.f));
    if (ImGui::Begin("Viewport")) {
      viewportSize = ImGui::GetWindowSize();
      ImGui::Image(rt);
    }
    ImGui::End();
    ImGui::PopStyleVar();

    window.clear();
    window.draw(shape);
    ImGui::SFML::Render(window);
    window.display();
  }

  ImGui::SFML::Shutdown();

  return 0;
}
